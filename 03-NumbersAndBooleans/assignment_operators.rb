# ASSIGNMENT OPERATORS

a = 10

p a + 5

# reassigning to a brand new variable
# reminder: everything to the right of the equal sign is evaluated first
b = a + 5
p b
# a = a + 5
# shorthand
a += 5
p a

puts
# shorthand exists for all mathmatical operators

b = 100
p b
# b = b - 50
b -= 50
p b

puts

c = 3
p c
# c = c * 4
c *= 4
p c

puts

# /= %= **=

d = 12
p d
# d = d / 3
d /= 3
p d 

puts

e = 7
p e
# e = e % 2
e %= 2
p e 

puts

f = 2
p f 
# f = f ** 3
f **= 3
